const PACKAGE_IDS = new Set(DEFAULT_WEBSITE_PACKAGES.map((pkg) => pkg.id));

function isValidCategory(value: unknown): value is WebsitePackageCategory {
  return value === "bridal" || value === "event";
}

function sanitizePackage(raw: unknown, fallback: WebsitePackage): WebsitePackage {
  if (!raw || typeof raw !== "object") return fallback;

  const item = raw as Record<string, unknown>;
  if (item.id !== fallback.id) return fallback;

  const title = typeof item.title === "string" ? item.title.trim() : "";
  const description =
    typeof item.description === "string" ? item.description.trim() : "";
  const priceLabel =
    typeof item.priceLabel === "string" ? item.priceLabel.trim() : "";

  return {
    id: fallback.id,
    category: isValidCategory(item.category) ? item.category : fallback.category,
    title: title || fallback.title,
    description: description || fallback.description,
    priceLabel: priceLabel || fallback.priceLabel,
  };
}

export function mergeWebsitePackages(stored: unknown): WebsitePackage[] {
  if (!Array.isArray(stored)) return [...DEFAULT_WEBSITE_PACKAGES];

  const byId = new Map(
    stored
      .map((item) => {
        if (!item || typeof item !== "object") return null;
        const id = (item as WebsitePackage).id;
        return typeof id === "string" ? ([id, item] as const) : null;
      })
      .filter(Boolean) as [string, unknown][],
  );

  return DEFAULT_WEBSITE_PACKAGES.map((fallback) =>
    sanitizePackage(byId.get(fallback.id), fallback),
  );
}

export function normalizeWebsitePackages(input: unknown): WebsitePackage[] {
  if (!Array.isArray(input)) {
    throw new Error("Website packages must be an array.");
  }

  const cleaned = DEFAULT_WEBSITE_PACKAGES.map((fallback) => {
    const raw = input.find(
      (item) =>
        item &&
        typeof item === "object" &&
        (item as WebsitePackage).id === fallback.id,
    );
    const pkg = sanitizePackage(raw, fallback);

    if (!pkg.title) {
      throw new Error(`Package title is required for "${fallback.title}".`);
    }
    if (!pkg.priceLabel) {
      throw new Error(`Price is required for "${pkg.title}".`);
    }

    return pkg;
  });

  const unknown = input.filter(
    (item) =>
      item &&
      typeof item === "object" &&
      typeof (item as WebsitePackage).id === "string" &&
      !PACKAGE_IDS.has((item as WebsitePackage).id),
  );

  if (unknown.length > 0) {
    throw new Error("One or more package IDs are not recognised.");
  }

  return cleaned;
}

export function packagesByCategory(packages: WebsitePackage[]) {
  return {
    bridal: packages.filter((pkg) => pkg.category === "bridal"),
    event: packages.filter((pkg) => pkg.category === "event"),
  };
}
